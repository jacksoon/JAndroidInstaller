    public JRomListItem(JRomRecordEntry record, JAndroidRomList par) {
        initComponents();
        this.parents = par;
        this.setSize(640, 140);
        this.setLocalRecord(record);
        this.lblName.setText(this.getLocalRecord().getRomName());
        this.lblDate.setText("发布日期：" + this.getLocalRecord().getRomMakeDate());
        this.lblMaker.setText("发布者：" + this.getLocalRecord().getRomMaker());
        if (this.getLocalRecord().getRomInfo().contains("[br]")) {
            this.lblInfo.setText("<html>" + this.getLocalRecord().getRomInfo().replace("[br]", "<br>") + "</html>");
        } else {
            this.lblInfo.setText(this.getLocalRecord().getRomInfo());
        }

        if (this.getLocalRecord().getRomIcon() != null && new File(this.getLocalRecord().getRomIcon()).exists()) {
            try {
                this.plImage.setImageIcon(new ImageIcon(ImageIO.read(new FileInputStream(this.getLocalRecord().getRomIcon()))), Boolean.TRUE);
            } catch (IOException ex) {
                Logger.getLogger(JRomListItem.class.getName()).log(Level.SEVERE, null, ex);
                this.plImage.setImageIconFromResource("/JAndroidInstaller/UIImage/romitem.png", true);
            }
        } else {
            this.plImage.setImageIconFromResource("/JAndroidInstaller/UIImage/romitem.png", true);
        }

        if (this.getLocalRecord().getRomFile() != null && !this.getLocalRecord().getRomFile().isEmpty()) {
            if (this.getLocalRecord().getRomFile().startsWith("http:")) {
                this.btnDownload.setEnabled(true);
                if (this.parents.existRomInQueue(this.getLocalRecord().getRomFile())) {
                    this.btnDownload.setButtonText(">>停止下载");
                } else {
                    this.btnDownload.setButtonText(">>下载固件");
                }
            } else {
                this.btnDownload.setButtonText("未开放下载");
                this.btnDownload.setEnabled(false);
            }
        } else {
            this.btnDownload.setButtonText("未开放下载");
            this.btnDownload.setEnabled(false);
        }
    }

