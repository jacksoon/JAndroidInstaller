    private void btnDownloadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDownloadMouseClicked
        // TODO add your handling code here:
        if (this.btnDownload.getButtonText().contains("停止下载"))
        {
            this.parents.removeRomFromQueue(this.getLocalRecord().getRomFile());
        } else {
            if (this.getLocalRecord().getRomFile() != null && !this.getLocalRecord().getRomFile().isEmpty()) {
                if (this.getLocalRecord().getRomFile().startsWith("http:")) {
                    this.parents.downloadRomFile(this.getLocalRecord().getRomFile());
                } else {
                    this.parents.printInfo("对不起，此固件没有开放下载！");
                }
            } else {
                this.parents.printInfo("对不起，此固件没有开放下载！");
            }
        }
    }//GEN-LAST:event_btnDownloadMouseClicked
